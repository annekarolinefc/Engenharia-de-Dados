## Precisa de pelo menos duas condições para que algo ocorra - Duas coisas acontecem de forma simultanea 
print(f'1>2>3: {1>2>3}') #False
print(f'1<2<3: {1<2<3}') #True
print(f'1<2<3 or 1>2>3: {1<2<3 or 1>2>3}') #True
print(f'1<2<3 and 1>2>3: {1<2<3 and 1>2>3}') #False
print(f'(2>1 or 2>3) or 2>11 or 2>3: {(2>1 or 2>3) or 2>11 or 2>3}') #True
print(f'True and True: {True and True}')
print(f'False and False: {False and False}')